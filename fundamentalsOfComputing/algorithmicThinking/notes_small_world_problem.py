###The Small World Problem

#How can we algorithmically test if the world is small?
#If the world is small, then people are highly connected so there are few acquaintances between most people.
#A computer network is small if edges don't have many nodes in order to connect them in the smallest path.
#If the human world is small, then people know each other through few mutual connections and diseases/ideas can spread easily.

#1) Understand the problem - take any two random Facebook accounts and see the smallest path between those two individuals.
#2) Formulate the problem - A network of individual nodes and their connections are edges in connection graph. Come up with distribution of minimal, pairwise distances between any two nodes.