###Pseudocode

#Once designed, programs in this course are implemented in Python.
#However, pseudocode is a useful tool to arrive to a solution.
#Pseudocode is a mix of a programming and human language in an effort to understand a computational problem at a high level.
#Pseudocode tells the programmer what the algorithm is meant to do, not why.
#The higher level pseudocode is, the easier it is to understand, but it is further away from being able to understand its machine learning characteristics.