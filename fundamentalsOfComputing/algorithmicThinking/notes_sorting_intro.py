### Sorting Introduction

# If any two items in a set can be compared, then the set is total ordered.
# The standard way that strings are ordered is lexical ordering.
# The lexical ordering of strings says that a string is "smaller" than another if, they have the same starting,...
# ...letters, the smaller string has a smaller amount of letters (a < aaa).
# A partial ordering exists on a set if only a subset of the set can be ordered.
# An arbitrary order can be induced on a set, but it must be natural/meaningful for it to be totally ordered.
# The sorting problem is to find an algorithm such that for n elements, item 1 is smaller than 2, 2 smaller than 3, ... , (n - 1) smaller than n.

