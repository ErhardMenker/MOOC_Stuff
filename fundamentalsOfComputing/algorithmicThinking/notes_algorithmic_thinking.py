###Algorithmic Thinking

##Algorithmic thinking is a five step process:
#1) State the problem and process (what is the input and output?)
#2) Formulate how the input is structured and how it can be mapped to output
#3) Develop an algorithm to solve the problem
#4) Implement the algorithm with neither syntax errors nor inefficiencies
#5) Present the output in a way that is optimal given the original question and target audience

