## Modules
# Modules are accessed by import <modulename>.
# Operations in the module can be accessed calling <modulename>.<operationname>
# Example: print math.sqrt(25) (returns 5, by calling the sqrt function on the math module on the input 25).
# Calling dir on a module (or any type) says what methods are available to that class.
# Calling as after the import statement allows a module to be accessed via a new name.
# Example: import random as rand (we can access the random module by calling rand).
# Module pseudonyms should be used when the module name is very inconvenient/awkward.