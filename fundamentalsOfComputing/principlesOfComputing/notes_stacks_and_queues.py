### Stacks and Queues

# Stacks and queues hold information between computations and restrict those computations.
# Stacks are LIFO structures (last in - first out), so the last element into the structure is the first one to be retrieved.
# Queues are FIFO structures (first in - first out), so the first element in the structure is the first to be retrieved.
# Pushing is the act of placing an element in a data structure, popping is the act of removing it.